CREATE DATABASE IF NOT EXISTS concierto_milenial;
USE concierto_milenial;

CREATE TABLE artistas (
    id_artista INT PRIMARY KEY,
    nombre_artista VARCHAR(50) NOT NULL,
    biografia VARCHAR(2000),
    oyentes INT NOT NULL,
    reproducciones INT NOT NULL
);

CREATE TABLE canciones (
    id_cancion VARCHAR(80) PRIMARY KEY,
    titulo VARCHAR(150) NOT NULL,
    anio INT,
    id_artista INT,
    FOREIGN KEY (id_artista) REFERENCES artistas(id_artista)
);

CREATE TABLE generos (
    id_genero INT PRIMARY KEY,
    genero VARCHAR(50) NOT NULL
);

CREATE TABLE fk_id_cancion_id_genero (
    id_cancion VARCHAR(80),
    id_genero INT,
    PRIMARY KEY (id_cancion, id_genero),
    FOREIGN KEY (id_cancion) REFERENCES canciones(id_cancion),
    FOREIGN KEY (id_genero) REFERENCES generos(id_genero)
);

CREATE TABLE artistas_similares (
    id_artista_similar INT PRIMARY KEY,
    nombre_artista_similar VARCHAR(50) NOT NULL
);

CREATE TABLE fk_id_artista_id_artista_similar (
    id_artista INT,
    id_artista_similar INT,
    PRIMARY KEY (id_artista, id_artista_similar),
    FOREIGN KEY (id_artista) REFERENCES artistas(id_artista),
    FOREIGN KEY (id_artista_similar) REFERENCES artistas_similares(id_artista_similar)
);

